package com.yingnet;

public class Dto {

	private String id;
	private String name;
	private String teltag;
	private String emailtag;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getTeltag() {
		return teltag;
	}

	public void setTeltag(String teltag) {
		this.teltag = teltag;
	}

	public String getEmailtag() {
		return emailtag;
	}

	public void setEmailtag(String emailtag) {
		this.emailtag = emailtag;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
